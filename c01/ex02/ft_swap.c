#include<stdio.h>
void	ft_swap(int *a, int *b)
{
	int temp;
	temp = *a;
	*a = *b;
	*b = temp;
}
int main()
{
	int x;
	int y;
	x = 100;
	y = 200;
	ft_swap(&x , &y);
	printf("x = %d\n", x);
	printf("y = %d\n", y);

}
